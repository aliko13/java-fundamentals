public class MathUtilityExample {
    public static void main(String[] args) {
        int a = -10;
        double x = 3.7;

        System.out.println("Absolute: " + Math.abs(a));
        System.out.println("Ceil: " + Math.ceil(x));
        System.out.println("Floor: " + Math.floor(x));
        System.out.println("Round: " + Math.round(x));
        System.out.println("Pow: " + Math.pow(2, 3));
        System.out.println("Sqrt: " + Math.sqrt(16));
        System.out.println("Random: " + Math.random());  // 0.0 - 1.0
    }
}
